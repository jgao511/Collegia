const EMPTY_SCHOOL_ENRICHMENT = makeSchoolEnrichment({});

function mergeSchoolEnrichmentProfile(name, seed) {
  const enrichment = makeSchoolEnrichment(seed);
  if (!window.schoolKnowledgeBase[name]) {
    window.schoolKnowledgeBase[name] = {
      aliases: seed.aliases || [],
      type: seed.type || "College or university",
      region: seed.region || "",
      setting: seed.setting || "",
      strongestMajors: seed.knownAreas || [],
      applicantFitSignals: ["Major-specific preparation", "Coursework aligned with intended major", "Activities with clear evidence", "Writing that explains school fit"],
      institutionalPersonality: `${name} has starter enrichment data in Collegia. Verify school-specific details against official university pages before publishing.`,
      fitNotes: "Use enrichment data only when relevant to the applicant's intended major."
    };
  }

  const profile = window.schoolKnowledgeBase[name];
  const existing = profile.schoolEnrichment || {};
  profile.schoolEnrichment = {
    ...EMPTY_SCHOOL_ENRICHMENT,
    ...existing,
    ...enrichment,
    majorSpecificAdvice: {
      ...EMPTY_SCHOOL_ENRICHMENT.majorSpecificAdvice,
      ...(existing.majorSpecificAdvice || {}),
      ...enrichment.majorSpecificAdvice
    },
    specificRecommendationIdeas: {
      ...EMPTY_SCHOOL_ENRICHMENT.specificRecommendationIdeas,
      ...(existing.specificRecommendationIdeas || {}),
      ...enrichment.specificRecommendationIdeas
    },
    reportPhrases: {
      ...EMPTY_SCHOOL_ENRICHMENT.reportPhrases,
      ...(existing.reportPhrases || {}),
      ...enrichment.reportPhrases
    },
    verification: {
      ...ENRICHMENT_VERIFICATION,
      ...(existing.verification || {}),
      ...enrichment.verification
    }
  };
}

