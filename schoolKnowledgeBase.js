function inferRelevantMajors(text) {
  const value = String(text || "");
  const tags = [];
  const add = (...items) => tags.push(...items);
  if (/engineer|comput|technology|information|data|robot|AI|systems|software|hardware|polytechnic|science/i.test(value)) add("Engineering", "Computer Science", "Data Science");
  if (/business|management|commerce|finance|econom|policy|government|international|political|social science|entrepreneur|market|analytics|leadership|foreign service/i.test(value)) add("Economics", "Business", "Political Science", "Public Policy", "International Relations");
  if (/biology|bio|health|medical|medicine|nursing|neuro|chemistry|pre-med|public health|life science|science|rehabilitation|clinical/i.test(value)) add("Biology", "Pre-Med", "Public Health", "Neuroscience", "Health Sciences");
  if (/arts|media|film|journalism|communication|music|theater|theatre|design|architecture|humanities|writing|literary|english|history|philosophy|creative/i.test(value)) add("Film", "Media", "Journalism", "Communications", "Design", "English", "Fine Arts");
  return [...new Set(tags)];
}

function makeSchoolEnrichment(seed) {
  const majorSpecificAdvice = {
    engineeringCS: seed.advice?.engineeringCS || "",
    businessSocialScience: seed.advice?.businessSocialScience || "",
    lifeSciencesHealth: seed.advice?.lifeSciencesHealth || "",
    humanitiesArtsMedia: seed.advice?.humanitiesArtsMedia || "",
    other: seed.advice?.other || ""
  };
  const ideas = seed.ideas || [];
  return {
    undergraduateSchools: (seed.undergraduateSchools || []).map((name) => ({ name, relevantMajors: inferRelevantMajors(name), notes: "" })),
    signaturePrograms: (seed.knownAreas || []).slice(0, 8).map((name) => ({ name, category: "Known area", relevantMajors: [name, ...inferRelevantMajors(name)], description: name, useInReportWhen: "Use when connected to the applicant's intended major or activities." })),
    experientialOpportunities: (seed.experientialOpportunities || []).map((name) => ({ name, category: "Experiential opportunity", relevantMajors: inferRelevantMajors(name), description: name, useInReportWhen: "Use when the applicant has related coursework, activities, or goals." })),
    studentOrganizationsOrCommonPathways: (seed.studentPathways || []).map((name) => ({ name, category: "Common pathway", relevantMajors: inferRelevantMajors(name), description: name, useInReportWhen: "Use as general fit context, not as a guaranteed opportunity." })),
    locationAdvantages: (seed.locationAdvantages || []).map((description) => ({ description, relevantMajors: inferRelevantMajors(description), useInReportWhen: "Use when location meaningfully supports the applicant's major or career interests." })),
    majorSpecificAdvice,
    specificRecommendationIdeas: {
      engineeringCS: ideas.filter((item) => /engineer|tech|coding|robot|prototype|hardware|software|AI|GitHub|design|product|sensor|startup|maker/i.test(item)).slice(0, 5),
      businessSocialScience: ideas.filter((item) => /business|econom|policy|market|finance|startup|case|data|operations|analysis|literacy|brief|industry/i.test(item)).slice(0, 5),
      lifeSciencesHealth: ideas.filter((item) => /health|biology|bio|public-health|medical|neuro|science|lab|service/i.test(item)).slice(0, 5),
      humanitiesArtsMedia: ideas.filter((item) => /film|media|portfolio|journalism|writing|arts|music|theater|design|story|communication/i.test(item)).slice(0, 5),
      other: ideas.slice(0, 5)
    },
    reportPhrases: {
      schoolFit: seed.reportPhrases?.schoolFit || [],
      majorFit: seed.reportPhrases?.majorFit || [],
      activitiesImpact: seed.reportPhrases?.activitiesImpact || [],
      suggestedNextSteps: seed.reportPhrases?.suggestedNextSteps || []
    },
    verification: ENRICHMENT_VERIFICATION
  };
}

